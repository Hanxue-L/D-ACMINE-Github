import json
import numpy as np
import pandas as pd
import os
import warnings
import optuna

# Machine Learning & Metrics
from sklearn.svm import SVC
from sklearn.ensemble import RandomForestClassifier
from sklearn.neural_network import MLPClassifier
from sklearn.neighbors import KNeighborsClassifier
from sklearn.model_selection import RepeatedStratifiedKFold, StratifiedKFold, cross_validate, train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import make_pipeline
from sklearn.metrics import make_scorer, confusion_matrix, matthews_corrcoef, f1_score, accuracy_score, recall_score, precision_score, roc_auc_score, roc_curve

# Suppress warnings for cleaner output
warnings.filterwarnings("ignore")
optuna.logging.set_verbosity(optuna.logging.WARNING)

# Set random seed
RANDOM_SEED = 42
np.random.seed(RANDOM_SEED)

# =============================================================================
# Helper Functions
# =============================================================================

def specificity_score(y_true, y_pred):
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    return tn / (tn + fp) if (tn + fp) > 0 else 0

def load_data():
    """
    Load extracted features and golden pairs mapping.
    Expects 'extracted_features.json' and 'golden_pairs.json' in the current directory.
    """
    if not os.path.exists("extracted_features.json") or not os.path.exists("golden_pairs.json"):
        print("Error: Input data files not found.")
        print("Please ensure 'extracted_features.json' (generated by dpv_feature_extraction.py)")
        print("and 'golden_pairs.json' (sample pairing definition) are in the current directory.")
        return None, None
        
    with open("extracted_features.json") as f:
        data = json.load(f)
    with open("golden_pairs.json") as f:
        pairs = json.load(f)
        
    h92 = data["miR-92a"]["Healthy"]
    h21 = data["miR-21"]["Healthy"]
    d92 = data["miR-92a"]["CRC"]
    d21 = data["miR-21"]["CRC"]
    
    perm_h = pairs["perm_h"]
    perm_d = pairs["perm_d"]
    
    X = []
    y = []
    
    # Healthy Group (Label=0)
    for i in range(len(h92)):
        if i >= len(perm_h): break
        f92 = h92[i]
        f21 = h21[perm_h[i]]
        row = [
            f92["Delta_I"], f21["Delta_I"],
            f92["Ep"], f21["Ep"],
            f92["Ah"], f21["Ah"],
            f92["FWHM"], f21["FWHM"]
        ]
        X.append(row)
        y.append(0)
        
    # CRC Group (Label=1)
    for i in range(len(d92)):
        if i >= len(perm_d): break
        f92 = d92[i]
        f21 = d21[perm_d[i]]
        row = [
            f92["Delta_I"], f21["Delta_I"],
            f92["Ep"], f21["Ep"],
            f92["Ah"], f21["Ah"],
            f92["FWHM"], f21["FWHM"]
        ]
        X.append(row)
        y.append(1)
        
    return np.array(X), np.array(y)

# =============================================================================
# Model Objectives (from generate_plot_data.py)
# =============================================================================

def get_svm_objective(trial):
    c = trial.suggest_float("C", 0.1, 100, log=True)
    gamma = trial.suggest_float("gamma", 0.001, 1, log=True)
    return make_pipeline(StandardScaler(), SVC(C=c, gamma=gamma, kernel='rbf', probability=True, random_state=RANDOM_SEED))

def get_rf_objective(trial):
    n_estimators = trial.suggest_int("n_estimators", 50, 500)
    max_depth = trial.suggest_int("max_depth", 3, 20)
    return make_pipeline(StandardScaler(), RandomForestClassifier(n_estimators=n_estimators, max_depth=max_depth, random_state=RANDOM_SEED))

def get_knn_objective(trial):
    n_neighbors = trial.suggest_int("n_neighbors", 3, 11)
    return make_pipeline(StandardScaler(), KNeighborsClassifier(n_neighbors=n_neighbors, weights='uniform'))

def get_mlp_objective(trial):
    hidden_layer_sizes = trial.suggest_categorical("hidden_layer_sizes", [(50,), (100,), (50, 50)])
    alpha = trial.suggest_float("alpha", 0.0001, 0.1, log=True)
    return make_pipeline(StandardScaler(), MLPClassifier(hidden_layer_sizes=hidden_layer_sizes, alpha=alpha, solver='lbfgs', max_iter=2000, random_state=RANDOM_SEED))

models_config = {
    "SVM": get_svm_objective,
    "RF": get_rf_objective,
    "KNN": get_knn_objective,
    "MLP": get_mlp_objective
}

def main():
    print("==================================================")
    print("      D-ACMINE ML Training & Evaluation Script    ")
    print("      (Reproducing Manuscript Methodology)        ")
    print("==================================================")
    
    # 1. Load Data
    X, y = load_data()
    if X is None:
        return
        
    print(f"Data Loaded: {len(X)} samples with 8 features.")
    print(f"Class distribution: {np.bincount(y)} (0=Healthy, 1=CRC)")
    
    # 2. Nested Repeated CV Setup (Strictly matching generate_plot_data.py)
    # Outer CV: Repeated Stratified 5-Fold x 5 Repeats (Total 25 folds)
    outer_cv = RepeatedStratifiedKFold(n_splits=5, n_repeats=5, random_state=RANDOM_SEED)
    
    metrics_per_fold = []
    
    print("\nStarting Nested Cross-Validation (5 Repeats x 5 Folds = 25 iterations)...")
    
    fold_idx = 0
    for train_idx, test_idx in outer_cv.split(X, y):
        fold_idx += 1
        X_train, X_test = X[train_idx], X[test_idx]
        y_train, y_test = y[train_idx], y[test_idx]
        
        print(f"Processing Fold {fold_idx}/25...")
        
        for model_name, objective_func in models_config.items():
            # Inner CV for Hyperparameter Tuning
            # Inner CV: Stratified 3-Fold (Not Repeated)
            study = optuna.create_study(direction="maximize", sampler=optuna.samplers.TPESampler(seed=RANDOM_SEED))
            
            def objective(trial):
                clf = objective_func(trial)
                inner_cv = StratifiedKFold(n_splits=3, shuffle=True, random_state=RANDOM_SEED)
                scores = []
                for t_idx, v_idx in inner_cv.split(X_train, y_train):
                    clf.fit(X_train[t_idx], y_train[t_idx])
                    # Optimize for AUC
                    scores.append(roc_auc_score(y_train[v_idx], clf.predict_proba(X_train[v_idx])[:, 1]))
                return np.mean(scores)

            # Optimize (20 trials as per manuscript)
            optuna.logging.set_verbosity(optuna.logging.WARNING)
            study.optimize(objective, n_trials=20) 
            best_params = study.best_params
            
            # Refit Best Model on Full Training Set of this Fold
            if model_name == "SVM":
                 best_model = make_pipeline(StandardScaler(), SVC(**best_params, kernel='rbf', probability=True, random_state=RANDOM_SEED))
            elif model_name == "RF":
                 best_model = make_pipeline(StandardScaler(), RandomForestClassifier(**best_params, random_state=RANDOM_SEED))
            elif model_name == "KNN":
                 best_model = make_pipeline(StandardScaler(), KNeighborsClassifier(**best_params, weights='uniform'))
            elif model_name == "MLP":
                 best_model = make_pipeline(StandardScaler(), MLPClassifier(**best_params, solver='lbfgs', max_iter=2000, random_state=RANDOM_SEED))
            
            best_model.fit(X_train, y_train)
            
            # Predict on Test Set
            y_prob = best_model.predict_proba(X_test)[:, 1]
            
            # Threshold Tuning (on Train Set only!)
            # Find best threshold on training set maximizing Youden's J
            y_train_prob = best_model.predict_proba(X_train)[:, 1]
            fpr, tpr, thresholds = roc_curve(y_train, y_train_prob)
            j_scores = tpr - fpr
            best_threshold = thresholds[np.argmax(j_scores)]
            
            y_pred = (y_prob >= best_threshold).astype(int)
            
            # Calculate Metrics
            auc = roc_auc_score(y_test, y_prob)
            f1 = f1_score(y_test, y_pred)
            acc = accuracy_score(y_test, y_pred)
            tn, fp, fn, tp = confusion_matrix(y_test, y_pred).ravel()
            sens = recall_score(y_test, y_pred) # Sensitivity = Recall
            spec = tn / (tn + fp) if (tn + fp) > 0 else 0
            prec = precision_score(y_test, y_pred, zero_division=0)
            mcc = matthews_corrcoef(y_test, y_pred)
            
            metrics_per_fold.append({
                "model": model_name,
                "fold": fold_idx,
                "auc": auc,
                "f1": f1,
                "accuracy": acc,
                "sensitivity": sens,
                "specificity": spec,
                "precision": prec,
                "mcc": mcc
            })

    # 3. Aggregate and Report Results
    df_metrics = pd.DataFrame(metrics_per_fold)
    summary = df_metrics.groupby("model")[['auc', 'f1', 'accuracy', 'sensitivity', 'specificity', 'mcc']].agg(['mean', 'std']).reset_index()
    
    # Flatten columns for display
    summary.columns = ['_'.join(col).strip() if col[1] else col[0] for col in summary.columns.values]
    
    # Sort by AUC mean
    summary = summary.sort_values(by="auc_mean", ascending=False)
    
    print("\n==================================================")
    print("      Final Evaluation Results (Mean ± SD)        ")
    print("==================================================")
    
    # Format output similar to manuscript table
    output_cols = ['model', 'auc_mean', 'auc_std', 'f1_mean', 'f1_std', 'accuracy_mean', 'accuracy_std']
    print(summary[output_cols].to_string(index=False, float_format="%.4f"))
    
    # Save detailed metrics
    df_metrics.to_csv("metrics_per_fold.csv", index=False)
    summary.to_csv("metrics_summary.csv", index=False)
    print("\nDetailed results saved to 'metrics_per_fold.csv' and 'metrics_summary.csv'")

if __name__ == "__main__":
    main()
